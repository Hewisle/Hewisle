  <h2>Brand Storytelling</h2>

  <p>
    Brand Storytelling is een manier van communicatie tussen merk en doelgroep
    op een narratieve manier, hierdoor bestaat er een emotionele band. Het is
    niet een verhaal over de oprichters van een bedrijf (behalve als het een
    verhaal is dat de doelgroep aanspreekt) en ook geen reclame om je product te
    verkopen, Maar een boodschap die jij wilt samenvoegen aan je merk. De kern
    van Brand Storytelling is dat je doelgroep kan identificeren met het verhaal
    en vervolgens je merk relatie sterker maakt.
  </p>

