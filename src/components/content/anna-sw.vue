		Het maken van de character designs ging volgens een vaste structuur. Eerst ging ik brainstormen, hierbij tekende ik alle personages die in me op kwamen. Uiteindelijk koos ik een personage om verder uit te werken. Daarna ging ik schetsen. Ik tekende het personage in verschillende houdingen en van verschillende kanten, hierdoor kreeg ik een beter beeld van het design en de anatomie. Ook maakte ik een moodboard bij elk personage, dit hielp me bij het kiezen van de kleuren en de sfeer die bij het personage past. Ik experimenteerde met verschillende kleurencombinaties door een aantal color studies te maken. Nadat ik een kleurenpalet had gekozen maakte ik de definitieve illustraties: een gedetailleerde illustratie van het personage, een illustratie van het personage in verschillende poses, een illustratie van voorwerpen die iets zeggen over het personage en een grote illustratie van de omgeving waarin het personage leeft. Ten slotte scheef ik ook een beschrijving bij elk personage.
	</p>
